/**
 * Returns the custom SVG icon component for a transport type,
 * or null if no custom icon exists yet (falls back to emoji).
 */
const CUSTOM_ICONS: Partial<Record<TransportType, (props: TransportIconProps) => React.JSX.Element>> = {
  plane: PlaneIcon,
  car: CarIcon,
};

export function TransportIcon({
  type,
  size = 24,
  color,
}: TransportIconProps & { type: TransportType }) {
  const IconComponent = CUSTOM_ICONS[type];
  if (IconComponent) {
    return <IconComponent size={size} color={color} />;
  }
  return null;
}

export function hasCustomIcon(type: TransportType): boolean {
  return type in CUSTOM_ICONS;
}
